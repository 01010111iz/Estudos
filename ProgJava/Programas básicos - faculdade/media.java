class Media
{
    public static void main (String entrada[])
    {
        double n, n1, n2, n3, media;
        
        n = 2000;
        n1 = 2554.02;
        n2 = 1987.35;
        n3 = 3598.75;
        media = (n+n1+n2+n3)/4;

        System.out.println("A média dos salários R$ 2000.00, R$ 2554.02, R$ 1987.35 e R$ 3598.75 é: " + media);
        System.exit(0);
    }
}